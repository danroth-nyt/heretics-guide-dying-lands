/**
 * Get all tables for a specific location type
 */
export function getLocationTables(locationType: LocationType): LocationTables {
  return locationTables[locationType] || {};
}

/**
 * Get a specific table for a location type
 */
export function getLocationTable(locationType: LocationType, tableName: string): Table | undefined {
  const tables = locationTables[locationType];
  return tables ? tables[tableName] : undefined;
}
